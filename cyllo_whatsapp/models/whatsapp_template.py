    @api.constrains('attachment_ids', 'header_type')
    def _check_attachment_ids(self):
        """
          Check constraints on attachment_ids and header_type.

          This method ensures that the attachments used as headers adhere to the constraints:
          1. For header types 'image' or 'document', only one attachment is allowed per template.
          2. The attached file types must be supported for the given header type.

          Raises:
              ValidationError: If the constraints are violated.

          Returns:
              None
        """
        for record in self:
            if record.header_type in ['image', 'document']:
                if len(record.attachment_ids) > 1:
                    record.attachment_ids = [record.attachment_ids.ids[0]]
                    raise ValidationError(
                        _('You can only use one header attachment for '
                          'each template'))
                supported_mimetypes = self.env[
                    'whatsapp.message']._COMPATIBLE_ATTACHMENT_TYPE.values()
                supported_type = [item for category_values in
                                  supported_mimetypes for item in
                                  category_values]
                unsupported_types = record.attachment_ids.filtered(
                    lambda att: att.mimetype and att.mimetype
                                not in supported_type)
                if unsupported_types:
                    file_types = unsupported_types.mapped('mimetype')
                    raise ValidationError(
                        _("The %(file_type)s file type is not supported for header type %(header_type)s",
                          file_type=file_types, header_type=record.header_type))

    @api.depends('header_text', 'body')
    def _compute_variable_ids(self):
        """
           Compute variable IDs based on header_text and body content.

           The computed variable IDs are stored in the 'variable_ids' field of
           the template.

           Returns: None """
        for record in self:
            pattern = r'{{(.*?)}}'
            record.variable_ids = False
            if record.header_type == 'text':
                header_variable = re.search(pattern, record.header_text) if (
                    record.header_text) else None
                if header_variable:
                    record.variable_ids = [fields.Command.create({
                        'display_name': 'header',
                        'type': 'header',
                        'sample_value': 'Sample Value',
                        'variable_value': 'name'
                    })]
            body_variable = re.search(pattern,
                                      record.body) if record.body else None
            body_variable_collection = re.findall(pattern,
                                                  record.body) if body_variable else None
            if body_variable_collection:
                record.variable_ids = [fields.Command.create({
                    'display_name': 'body- {' + rec + '}',
                    'type': 'body',
                    'template_id': record.id,
                    'sample_value': 'Sample Value',
                    'variable_value': 'name'
                }) for rec in body_variable_collection]

    @api.constrains('header_text')
    def _check_header_text(self):
        """
          Check constraints related to header text in WhatsApp templates.

          Raises:
              ValidationError: If the number of variables in the header text is
              greater than one.

          Returns:
              None
          """
        if self.header_type == 'text':
            pattern = r'{{(.*?)}}'
            pattern_present = re.search(pattern,
                                        self.header_text) if self.header_text else False
            matches = re.findall(pattern,
                                 self.header_text) if pattern_present else 0
            variable_count = len(matches) if matches else 0
            if variable_count > 1:
                raise ValidationError(
                    _('Only one variable allowed for header text'))

    @api.depends('name')
    def _compute_template_name(self):
        """
        Compute the template name based on the 'name' field.

        Returns:
            None
        """
        for record in self:
            db_uuid = self.env['ir.config_parameter'].sudo().get_param(
                'database.uuid')
            db_uuid_formatted = db_uuid.replace('-', '')
            template_name = str(record.name).replace(" ", "_")
            record.template_name = f"{template_name}_{db_uuid_formatted}"

    @property
    def get_whatsapp_account_details(self):
        """
        Retrieve WhatsApp account details for the current user.

        Returns:
            dict: Dictionary containing WhatsApp account details for the current
             user. The dictionary includes 'cloud_token', 'account_uid',
            'phone_uid', and 'app_uid' fields.

        Raises:
            ValidationError: If any of the required WhatsApp account details
            (cloud token, account UID, phone UID, or app UID) is missing for
            the current user.
        """
        if not (self.env.user.token and self.env.user.account_uid and
                self.env.user.phone_uid and self.env.user.app_uid):
            raise ValidationError(
                _('Whatsapp account configuration is required for sending messages. \n '
                  'Go to /Settings/Users & Companies/Users/Whatsapp Account'))
        return {
            'cloud_token': self.env.user.token,
            'account_uid': self.env.user.account_uid,
            'phone_uid': self.env.user.phone_uid,
            'app_uid': self.env.user.app_uid,
        }

    @api.ondelete(at_uninstall=False)
    def _unlink_if_delete_or_draft(self):
        """Override the unlink method to prevent deletion of whatsapp template.
            This method is used to prevent the deletion of template that are
            not in the 'draft' or 'deleted' states.
            @raises: UserError if any template being unlinked is not in 'draft'
            or 'deleted' state.
            @return: None """
        if any(template.state not in ('draft', 'deleted', 'rejected') for
               template in self):
            raise UserError(
                _('You cannot delete a whatsapp template which is not draft, rejected ,or deleted!'))

    # Template creation
    def _get_header_handle(self):
        """ Get the header handle for the template."""
        if not self.attachment_ids:
            raise UserError(_('Please select sample document for template'))
        if self.attachment_ids:
            cloud_token = self.get_whatsapp_account_details['cloud_token']
            app_uid = self.get_whatsapp_account_details['app_uid']
            url = f'https://graph.facebook.com/v18.0/{app_uid}/uploads'
            for record in self.attachment_ids:
                base = int(
                    math.floor(math.log(self.attachment_ids.file_size, 1024)))
                file_length = round(
                    self.attachment_ids.file_size / (1024 ** base))
                params = {
                    'file_length': file_length,
                    'file_type': record.mimetype,
                    'access_token': cloud_token
                }
                if self.header_type == 'document' and record.mimetype != 'application/pdf':
                    raise ValidationError(
                        _('Only pdf type documents are supported for media template'))
                if self.header_type == 'image' and record.mimetype not in [
                    'image/png', 'image/jpeg']:
                    raise ValidationError(
                        _('Only png or jpeg types are supported for image type media template'))
            response = requests.post(url, params=params)
            if response.status_code != 200:
                raise ValidationError(_('Uploading the sample document failed'))
            upload_session_id = response.json().get('id')
            # Initiate Upload
            url = f"https://graph.facebook.com/v18.0/{upload_session_id}"
            headers = {"Authorization": f"OAuth {cloud_token}",
                       "file_offset": "0"}
            for attachment in self.attachment_ids:
                fname = attachment.store_fname
                file_path = attachment._full_path(fname)
                file_name = attachment.name
                file_type = attachment.mimetype
                files = {
                    "file": (file_name, open(file_path, "rb"), file_type, {})}
            response = requests.post(url, headers=headers, data=files)
            if response.status_code == 200:
                return response.json().get('h')
            else:
                raise ValidationError(_('Uploading the sample document failed'))

    def _create_header_components(self):
        """ Create header components for the template."""
        header = {"type": "HEADER", "format": self.header_type}
        if self.header_type == 'text':
            header["text"] = self.header_text
            header_sample = self.variable_ids.search(
                [('display_name', '=', 'header'),
                 ('template_id', '=', self.id)])
            if header_sample:
                header["example"] = {
                    "header_text": [header_sample.sample_value]}
        elif self.header_type in ['document', 'image']:
            header_handle = self._get_header_handle()
            header["example"] = {"header_handle": [header_handle]}
        return header

    def _create_body_components(self):
        """ Create body components for the template."""
        body = {"type": "BODY", "text": self.body}
        body_sample = self.variable_ids.search(
            [('display_name', 'ilike', 'body'),
             ('template_id', '=', self.id)]).mapped('sample_value')
        if body_sample:
            body["example"] = {"body_text": [body_sample]}
        return body

    def action_create_template(self):
        """ Create a WhatsApp template."""
        if self.header_type == 'image':
            if not self.binary_field_path:
                raise ValidationError(_('Please provide image file path'))
            parts = self.binary_field_path.split('.')
            model = self.model_id.id
            flag = 0
            for part in parts:
                flag += 1
                if model:
                    field = self.env['ir.model.fields'].sudo().search(
                        [('name', '=', part), ('model_id', '=', model)])
                    if field:
                        if flag != len(parts):
                            if field.ttype == 'many2one':
                                model = self.env['ir.model'].sudo().search(
                                    [('model', '=', field.relation)]).id
                            else:
                                model = False
                        else:
                            if field.ttype != 'binary':
                                return {
                                    'type': 'ir.actions.client',
                                    'tag': 'display_notification',
                                    'params': {
                                        'message': _(
                                            'The last field must be binary'),
                                        'type': 'warning',
                                    },
                                }
                else:
                    return {
                        'type': 'ir.actions.client',
                        'tag': 'display_notification',
                        'params': {
                            'message': _('Wrong path'),
                            'type': 'warning',
                        },
                    }
        cloud_token = self.get_whatsapp_account_details['cloud_token']
        account_uid = self.get_whatsapp_account_details['account_uid']
        headers = {'Authorization': f'Bearer {cloud_token}',
                   'Content-Type': 'application/json'}
        components = []
        if self.header_type != 'none':
            components.append(self._create_header_components())
        if self.body:
            components.append(self._create_body_components())
        if self.footer_text:
            footer = {"type": "FOOTER", "text": self.footer_text}
            components.append(footer)
        if self.is_button:
            cta = {
                "type": "BUTTONS",
                "buttons": [
                    {
                        "type": "url",
                        "url": self.button_url,
                        "text": self.button_name
                    },
                ]
            }
            components.append(cta)
        payload = {
            "name": self.template_name,
            "language": self.lang_id.code,
            "category": self.category,
            "components": components
        }
        response = requests.post(
            f"https://graph.facebook.com/v18.0/{account_uid}/message_templates",
            json=payload, headers=headers)
        if response.status_code != 200:
            error = response.json()['error']
            raise ValidationError(
                _('%s\n%s\n%s', error['message'], error['error_user_title'],
                  error['error_user_msg']))
        template_response = response.json()
        self.template_uid = template_response.get('id')
        if template_response.get('status'):
            if template_response.get('status') == 'REJECTED':
                self.is_reason_update = True
                self.reason = "Template format is not correct, please check."
            self.state = template_response.get('status').lower()

    # Template validation
    def action_get_template(self):
        """ Get the WhatsApp template."""
        cloud_token = self.get_whatsapp_account_details['cloud_token']
        account_uid = self.get_whatsapp_account_details['account_uid']
        url = f"https://graph.facebook.com/v17.0/{account_uid}/message_templates?name={self.template_name}"
        header = {'Authorization': f'Bearer {cloud_token}'}
        response = requests.get(url, headers=header)
        response_data = response.json()['data'][0]
        template_status = response_data['status']
        self.state = template_status.lower()

    # Sending template
    def _get_header_component(self, record, attachment, partner):
        """Get header components for sending a template."""
        header = {"type": "header"}
        cloud_token = self.get_whatsapp_account_details['cloud_token']
        phone_uid = self.get_whatsapp_account_details['phone_uid']
        payload = {
            "messaging_product": "whatsapp",
            "recipient_type": "individual",
            "to": partner.whatsapp_number
        }
        token = {'Authorization': f'Bearer {cloud_token}'}
        if self.header_type == 'text':
            header_variables = self.variable_ids.filtered(
                lambda line: line.display_name == 'header')
            if header_variables:
                variable_value = self._extract_variable_value(header_variables,
                                                              record)
                header["parameters"] = [
                    {"type": "text", "text": variable_value[0]}]
        if self.header_type == 'document':
            media_id = self.env['whatsapp.message'].generate_media_id(
                attachment, token, payload,
                phone_uid) if self.report_id else False
            if not media_id:
                raise ValidationError(_('Media ID is not generated.'))
            header["parameters"] = [{"type": "document",
                                     "document": {'id': media_id,
                                                  'filename': attachment.name}}]
        if self.header_type == 'image':
            current_value = record
            for attribute_name in self.binary_field_path.split('.'):
                if hasattr(current_value, attribute_name):
                    current_value = getattr(current_value, attribute_name)
                else:
                    current_value = None
                    break
            attachment_values = {
                'name': 'Partner Image',
                'type': 'binary',
                'datas': current_value or '',
                'res_model': self.model_id.model,
                'res_id': self.id,  # The ID of the record to associate with
            }
            attachment = self.env['ir.attachment'].create(attachment_values)
            if not attachment.store_fname:
                raise ValidationError(
                    _(f"This model Does not contain Attachment in {self.binary_field_path} field "))
            media_id = self.env['whatsapp.message'].generate_media_id(
                attachment, token, payload,
                phone_uid) if attachment else False
            header["parameters"] = [
                {"type": "image", "image": {'id': media_id}}]
        header_values = {'header': header, 'attachment': attachment}
        return header_values

    def _get_body_component(self, record):
        """ Get body components for sending a template."""
        body = {"type": "body"}
        body_variables = self.variable_ids.filtered(
            lambda line: line.display_name != 'header')
        if body_variables:
            variable_value = self._extract_variable_value(body_variables,
                                                          record)
            parameters = []
            for val in variable_value:
                parameters += {"type": "text", "text": val},
            body["parameters"] = parameters
        return body

    def action_send_template_marketing(self, record, attachment, partner,
                                       cloud_token, phone_uid):
        """ Send a WhatsApp template."""
        if not record:
            raise ValidationError(_('Record is missed'))
        headers = {
            'Authorization': f'Bearer {cloud_token}',
            'Content-Type': 'application/json', }
        components = []
        if self.header_type != 'none':
            header_values = self._get_header_component_marketing(
                record, attachment, partner, cloud_token, phone_uid)
            components.append(header_values['header'])
        if self.body:
            components.append(self._get_body_component(record))
        if self.is_button:
            cta_button = {
                "type": "button",
                "sub_type": "url",
                "index": "1",
            }
            components.append(cta_button)
        payload = {
            "messaging_product": "whatsapp",
            "recipient_type": "individual",
            "to": partner.whatsapp_number,
            "type": "template",
            "template": {
                'name': self.template_name,
                "language": {
                    "code": self.lang_id.code
                },
                "components": components
            }
        }
        response = requests.post(
            f"https://graph.facebook.com/v17.0/{phone_uid}/messages",
            json=payload, headers=headers)
        if response.status_code == 200:
            template_body = self._get_formatted_template_body(record=1,
                                                              demo_value=False)
            channel = self.env['whatsapp.channel'].search(
                [('sender_id', '=', self.env.user.partner_id.id),
                 ('partner_id', '=', partner.id)])
            message_values = {
                'is_template': True,
                'template_id': self.id,
                'flag': True,
                'message_uid': response.json()['messages'][0]['id'],
                'state': 'sent',
            }
            if self.header_type != 'none':
                message_values['message_type'] = self.header_type
                if self.header_type == 'text':
                    header = self._get_formatted_template_header(
                        record=1, demo_value=False)
                    template_body = header + template_body
                if header_values and header_values['attachment'] != None:
                    attachment = header_values['attachment']
                    message_values['attachment_id'] = attachment.id
                if self.header_type == 'image':
                    message_values['image'] = attachment.datas
            if self.is_button:
                message_values['is_button'] = True
                message_values['button_name'] = self.button_name
                message_values['button_url'] = self.button_url
            if channel:
                message_values['channel_id'] = channel.id
            else:
                channel_record = self.env['whatsapp.channel'].create({
                    'name': partner.name,
                    'partner_id': partner.id,
                    'sender_id': self.env.user.partner_id.id,
                    'user_id': self.env.user.id
                })
                message_values['channel_id'] = channel_record.id
            message_values['template_body'] = template_body
            self.env['whatsapp.message'].create(message_values)
            return message_values['message_uid']
        else:
            error_message = response.json()['error']['message']
            raise ValidationError(
                _(error_message))


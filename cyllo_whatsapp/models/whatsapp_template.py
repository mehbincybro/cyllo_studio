    @api.model
    def _modify_text_to_html(self, text_body):
        """
        Convert WhatsApp formatted text to HTML.
        This function takes a text in WhatsApp format and converts it into HTML format.
        It supports the following formatting styles:
        - Bold: *text*
        - Italics: _text_
        - Strikethrough: ~text~
        - Code: ```text```

        Parameters:
        - body_html (str): The text in WhatsApp format to be converted.

        Returns:
        Markup: The formatted HTML text.
        """
        html_body = str(plaintext2html(text_body))
        formatting_patterns = [(r'\*(.*)\*', '<b>\\1</b>'),
                               (r'~(.*)~', '<s>\\1</s>'),
                               (r'_(.*)_', '<i>\\1</i>'),
                               (r'```(.*)```', '<code>\\1</code>')]
        for pattern, replacement in formatting_patterns:
            html_body = re.sub(pattern, replacement, html_body)
        return Markup(html_body)

    def _extract_variable_value(self, variable_records, attribute):
        """ Extract variable values from the specified attribute."""
        substitute_values = []
        objects = attribute
        for rec in variable_records:
            attribute = objects
            for attr in rec.variable_value.split('.'):
                if not hasattr(attribute, attr):
                    raise ValidationError(
                        f"'{attribute}' object has no attribute '{attr}'")
                attribute = getattr(attribute, attr)
            substitute_values.append(str(attribute))
        return substitute_values

    def _get_formatted_template_body(self, demo_value, record):
        """Get formatted body and header with specified values.
        :param bool demo_value: if true, sample value is taken instead of
        variable value
        :return Markup:
        """
        body = self.body
        body_variable = self.variable_ids.filtered(
            lambda line: line.display_name != 'header')
        if demo_value:
            body_values = body_variable.mapped('sample_value')
        else:
            model = self.model_id.model
            source_record = self.env[model].browse(record)
            body_values = self._extract_variable_value(body_variable,
                                                       source_record)
        for i in range(1, len(body_values) + 1):
            placeholder = "{{" + str(i) + "}}"
            body = body.replace(placeholder, body_values[i - 1])
        return self._modify_text_to_html(f'{body}')

    def _get_formatted_template_header(self, demo_value, record):
        """ Get a formatted template header with specified values."""
        self.ensure_one()
        header = self.header_text
        if self.header_type == 'text' and self.header_text:
            header_variables = self.variable_ids.filtered(
                lambda line: line.display_name == 'header')
            if header_variables:
                if demo_value:
                    header = self.header_text.replace('{{1}}',
                                                      header_variables.sample_value)
                else:
                    model = self.model_id.model
                    source_record = self.env[model].browse(record)
                    model_values = self._extract_variable_value(
                        header_variables, source_record)
                    header = self.header_text.replace('{{1}}', model_values[0])
        formatted_header = str(plaintext2html(header))
        pattern = r"<p>"
        replacement = r"<p><b>"
        formatted_header = re.sub(pattern, replacement, formatted_header,
                                  count=1)
        return Markup(formatted_header)
